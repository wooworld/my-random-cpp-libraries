#include "exception.h"

//////////////////////////////////////////////////////////////////////
/// @fn Exception()
/// @brief -- Instantiates the m_error_code and m_error_message
///           variables in the exception
/// @param code -- The error code number
/// @param message -- The error message
//////////////////////////////////////////////////////////////////////
Exception::Exception(int _error_code, std::string _error_message) {

  // Instantiates m_error_code
  m_error_code = _error_code;
  
  // And m_error_message
  m_error_message = _error_message;

}

//////////////////////////////////////////////////////////////////////
/// @fn int error_code()
/// @return -- Returns m_error_code
//////////////////////////////////////////////////////////////////////
int Exception::error_code() {

  return m_error_code;
  
}

//////////////////////////////////////////////////////////////////////
/// @fn string error_message()
/// @return -- Returns m_error_message
//////////////////////////////////////////////////////////////////////
std::string Exception::error_message() {

  return m_error_message;
  
}
