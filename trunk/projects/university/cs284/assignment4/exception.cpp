#include "exception.h"

//////////////////////////////////////////////////////////////////////
/// @fn Exception()
/// @brief -- Instantiates the m_error_code and m_error_message
///           variables in the exception
/// @param code -- The error code number
/// @param message -- The error message
//////////////////////////////////////////////////////////////////////
Exception::Exception(const int code, const string message)
                    : m_error_code(code),
                      m_error_message(message) {

}

//////////////////////////////////////////////////////////////////////
/// @fn int error_code()
/// @return -- Returns m_error_code
//////////////////////////////////////////////////////////////////////
int Exception::error_code() const {

  return m_error_code;
  
}

//////////////////////////////////////////////////////////////////////
/// @fn string error_message()
/// @return -- Returns m_error_message
//////////////////////////////////////////////////////////////////////
string Exception::error_message() const {

  return m_error_message;
  
}
